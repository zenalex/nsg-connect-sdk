String _categoryLabel(NsgL10n l, String key) => switch (key) {
  'smileys' => l.emojiCategorySmileys,
  'gestures' => l.emojiCategoryGestures,
  'hearts' => l.emojiCategoryHearts,
  'animals' => l.emojiCategoryAnimals,
  'food' => l.emojiCategoryFood,
  'activity' => l.emojiCategoryActivity,
  'symbols' => l.emojiCategorySymbols,
  _ => key,
};

class _EmojiReactionPickerBody extends StatelessWidget {
  const _EmojiReactionPickerBody();

  @override
  Widget build(BuildContext context) {
    final l = NsgL10n.of(context);
    final maxHeight = MediaQuery.of(context).size.height * 0.6;
    return SafeArea(
      child: ConstrainedBox(
        constraints: BoxConstraints(maxHeight: maxHeight),
        child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            Padding(
              padding: const EdgeInsets.fromLTRB(16, 4, 16, 8),
              child: Text(
                l.emojiPickerTitle,
                style: Theme.of(context).textTheme.titleMedium,
              ),
            ),
            Flexible(
              child: CustomScrollView(
                slivers: [
                  for (final cat in _kCategories) ...[
                    SliverToBoxAdapter(
                      child: Padding(
                        padding: const EdgeInsets.fromLTRB(16, 8, 16, 4),
                        child: Align(
                          alignment: Alignment.centerLeft,
                          child: Text(
                            _categoryLabel(l, cat.labelKey),
                            style: TextStyle(
                              fontSize: 12,
                              fontWeight: FontWeight.w600,
                              color: Theme.of(
                                context,
                              ).colorScheme.onSurfaceVariant,
                            ),
                          ),
                        ),
                      ),
                    ),
                    SliverPadding(
                      padding: const EdgeInsets.symmetric(horizontal: 8),
                      sliver: SliverGrid(
                        gridDelegate:
                            const SliverGridDelegateWithMaxCrossAxisExtent(
                              maxCrossAxisExtent: 48,
                              mainAxisSpacing: 2,
                              crossAxisSpacing: 2,
                            ),
                        delegate: SliverChildBuilderDelegate((ctx, i) {
                          final emoji = cat.emojis[i];
                          return InkResponse(
                            onTap: () => Navigator.of(ctx).pop(emoji),
                            radius: 22,
                            child: Center(
                              child: Text(
                                emoji,
                                style: const TextStyle(fontSize: 26),
                              ),
                            ),
                          );
                        }, childCount: cat.emojis.length),
                      ),
                    ),
                  ],
                  const SliverToBoxAdapter(child: SizedBox(height: 12)),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}
