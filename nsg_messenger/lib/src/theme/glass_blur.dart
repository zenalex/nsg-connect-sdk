import 'dart:ui';

/// **Единая точка настройки силы blur в Glass-теме (issue #26).**
///
/// Дизайн задаёт sigma 18–28 в одиннадцати местах приложения. Раньше эти
/// числа стояли прямо в виджетах — чтобы попробовать «а если слабее?», надо
/// было править одиннадцать файлов, поэтому вопрос стоимости blur так и не
/// проверялся. Здесь значение дизайна проходит через один множитель.
///
/// **Зачем это вообще.** Профилирование на HONOR VNE-LX1 (Android 14,
/// PowerVR GE8320) показало: основная стоимость растеризации — фон
/// (77–95%, вылечен `RepaintBoundary` в `GlassBackground`), а blur — ещё
/// ~10%. Площадь blur уже ограничена: `ClipRect`/`ClipRRect` стоят вокруг
/// ВСЕХ одиннадцати узлов. `RepaintBoundary` вокруг самого `BackdropFilter`
/// не помогает — он по определению читает то, что под ним, и пересчитывается
/// при любом движении внизу. Поэтому единственный оставшийся рычаг — sigma.
///
/// **Как попробовать, не трогая код:**
/// ```
/// flutter build apk --release --dart-define=GLASS_BLUR_PCT=45
/// ```
/// `100` — как в дизайне (по умолчанию), `45` ≈ sigma 28→12.6, `0` —
/// эффективно без размытия (стекло станет просто полупрозрачным).
///
/// Стоимость blur растёт нелинейно по sigma, поэтому снижение до ~45%
/// заметно дешевле, чем «на 55% меньше». Визуально же разница между 28 и
/// 12 на glass-поверхностях небольшая — но это вопрос вкуса, поэтому
/// значение по умолчанию НЕ трогаем: решает владелец дизайна, сравнив
/// сборки.
///
/// Единая точка нужна ещё и на будущее: если когда-нибудь появится
/// «экономный режим графики», переключать надо будет здесь, а не
/// одиннадцать раз по виджетам.
const int kGlassBlurPercent = int.fromEnvironment(
  'GLASS_BLUR_PCT',
  defaultValue: 100,
);

/// Sigma с учётом [kGlassBlurPercent]. [designSigma] — значение из дизайна.
///
/// Ноль отдаём как есть: `ImageFilter.blur(0, 0)` — валидный no-op фильтр,
/// поверхность остаётся полупрозрачной, но без размытия.
double glassBlurSigma(double designSigma) {
  // Кламп применяется ВСЕГДА, а не только при масштабировании: отрицательная
  // sigma — неопределённое поведение у разных бэкендов рендера, и защита не
  // должна зависеть от того, задан define или нет.
  final scaled = kGlassBlurPercent == 100
      ? designSigma
      : designSigma * kGlassBlurPercent / 100;
  return scaled < 0 ? 0 : scaled;
}

/// Готовый фильтр для `BackdropFilter(filter: glassBlur(28))`.
///
/// Всегда используй его вместо `ImageFilter.blur(...)` напрямую — иначе
/// узел выпадет из единой настройки и переключатель перестанет быть
/// честным (часть поверхностей останется на дизайнерской sigma).
ImageFilter glassBlur(double designSigma) {
  final s = glassBlurSigma(designSigma);
  return ImageFilter.blur(sigmaX: s, sigmaY: s);
}
