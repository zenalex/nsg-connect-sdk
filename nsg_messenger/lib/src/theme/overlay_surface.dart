import 'package:flutter/painting.dart';

/// **Единая точка для фона всплывающих поверхностей (issue #43).**
///
/// В Glass-темах `colorScheme.surface` НАМЕРЕННО прозрачный — так обои
/// приложения просвечивают сквозь ленту (см. `chatista_theme.dart`,
/// комментарий «Surfaces are TRANSPARENT so wallpaper shows through»).
/// Побочный эффект: любой виджет, который берёт фон из `surface` по
/// умолчанию — `Material`, `Card`, `Dialog`, `showModalBottomSheet`,
/// `PopupMenuButton`, `DropdownMenu` — рисуется БЕЗ подложки. Пока такая
/// поверхность лежит в потоке страницы, это выглядит задуманно; но как
/// только она всплывает НАД контентом, сквозь неё видно текст под ней и
/// читать становится нечего.
///
/// Так уже ловили issue #29 (модалки QR и «новый звонок») и issue #43
/// (попап @-подсказок в композере). Оба раза чинили одинаково — руками
/// прописывали `Color(0xFF1F1A15).withValues(alpha: 0.97)`. К третьему
/// разу копипаста расползлась на два десятка мест в двух пакетах, поэтому
/// цвет живёт здесь: следующий такой баг чинится в одной строке, а не
/// поиском по репозиторию.
///
/// **Когда использовать.** Всё, что ВСПЛЫВАЕТ над контентом: диалоги,
/// bottom-sheet'ы, меню, оверлеи-подсказки. НЕ использовать для стекла в
/// потоке страницы (фон приложения, glass-панели, бабблы сообщений) —
/// там прозрачность и есть дизайн.
///
/// Оттенок — тёмно-коричневый «кофейный» из палитры CHATista, тот же, что
/// у `darkSurface` в `chatista_theme.dart`.
const Color kOverlayBaseInk = Color(0xFF1F1A15);

/// Фон всплывающей поверхности: меню, попапы, диалоги, оверлеи.
///
/// Alpha 0.97, а не 1.0 — умышленно: остаётся едва заметный намёк на обои
/// под поверхностью, поэтому попап не выглядит «дыркой» в стеклянной теме,
/// но текст под ним уже не читается и не мешает.
///
/// Не `const`: значение получается через `withValues`, чтобы БИТ-В-БИТ
/// совпадать с теми вызовами, что уже стоят по коду, — иначе миграция
/// старых мест на константу незаметно поменяла бы отрисовку.
final Color kOverlaySurface = kOverlayBaseInk.withValues(alpha: 0.97);

/// Фон крупного bottom-sheet'а — на волос прозрачнее [kOverlaySurface].
///
/// Лист занимает пол-экрана, и на такой площади чуть больше обоев снизу
/// читается как глубина, а не как грязь. Значение уже сложилось по коду
/// (0.96 у листов против 0.97 у диалогов) — здесь оно просто закреплено.
final Color kOverlaySheetSurface = kOverlayBaseInk.withValues(alpha: 0.96);
